function car(manuf, mod, col, feat) {
    if (col === void 0) { col = "Black"; }
    if (feat === void 0) { feat = "Sunroof"; }
    var obj = {
        manufacturer: manuf,
        model: mod,
        color: col,
        feature: feat
    };
    return obj;
}
